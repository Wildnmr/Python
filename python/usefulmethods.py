# Useful Methods

freecodecamp = "freecodecamp"

print(freecodecamp.capitalize() )# capitalize first character of the value

print(freecodecamp.upper()) # Upper case value

print(freecodecamp.lower()) #lower case value

print(freecodecamp.find("e")) # find e and return the first e index

print(freecodecamp.count("c")) # count how many c exist and return count

print(freecodecamp.isalnum()) # if value is alph-numeric
print(freecodecamp.isdigit() )# if digital
print(freecodecamp.isupper() )# if upper case
print(freecodecamp.lstrip("f") )# remove first letter
print(freecodecamp.rstrip("p") )# remove last letter
print(freecodecamp.split("c")) # return value character splited in a list by C
